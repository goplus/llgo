var _libcLDFlags = []string{
	"-nostdlib",
	"-ffunction-sections",
	"-fdata-sections",
}

var _libcCCFlags = []string{
	"-Oz",
	"-fno-builtin",
	// freestanding tells compiler this is a baremental project
	"-ffreestanding",
}

// withDefaultCCFlags appends default C compiler flags to the provided flags
func withDefaultCCFlags(ccflags []string) []string {
	return append(ccflags, _libcCCFlags...)
}

// GetNewlibESP32Config returns the configuration for downloading and building newlib for ESP32
func GetNewlibESP32Config() compile.LibConfig {
	return compile.LibConfig{
		Url:            "https://github.com/goplus/newlib/archive/refs/tags/esp-4.3.0_20250211-patch6.tar.gz",
		Name:           "newlib-esp32",
		Version:        "esp-4.3.0_20250211-patch6",
		ResourceSubDir: "newlib-esp-4.3.0_20250211-patch6",
	}
}

